document.querySelector('.menu-open').onclick = function () {
    document.querySelector('.burger-menu').classList.add('show');
}

document.querySelector('.close').onclick = function () {
    document.querySelector('.burger-menu').classList.remove('show');
}


//page2

document.querySelector('.open1').onclick = function () {
    document.querySelector('.open1').classList.remove('show-item')
    document.querySelector('.close1').classList.add('show-item')
    document.querySelector('.question1').classList.add('show-item')
}

document.querySelector('.close1').onclick = function () {
    document.querySelector('.close1').classList.remove('show-item')
    document.querySelector('.open1').classList.add('show-item')
    document.querySelector('.question1').classList.remove('show-item')
}

